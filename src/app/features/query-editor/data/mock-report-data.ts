const textAggregations = ['count'] as const;
const numberAggregations = ['count', 'sum', 'avg', 'min', 'max'] as const;
const dateAggregations = ['count', 'min', 'max'] as const;

function field(
  tableId: string,
  name: string,
  label: string,
  type: DataSourceField['type'],
  aggregations: DataSourceField['aggregations'],
): DataSourceField {
  const id = `${tableId}.${name}`;

  return {
    id,
    tableId,
    name,
    label,
    expression: `${tableId}.${name}`,
    type,
    nullable: false,
    aggregations,
  };
}

